    private void jBConfirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBConfirmarMouseClicked
        // TODO add your handling code here:
        currentUniversidad.setSIGLAS(jTSiglas.getText());
        currentUniversidad.setRECTOR(jTRector.getText());
        
        
        //Validaciones Expresiones Regulares
        
        switch(_mode){
            case "INS":
                _reloadParent = UniversidadModel.agregarUniversidad(currentUniversidad);
                break;
            case "UPD":
                _reloadParent = UniversidadModel.editarUniversidad(currentUniversidad);
                break;
            case "DEL":
                _reloadParent = UniversidadModel.eliminarUniversidad(currentUniversidad);
                break;
        }
        
        dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
    }//GEN-LAST:event_jBConfirmarMouseClicked

