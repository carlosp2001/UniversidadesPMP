    public UniversidadForm(String Mode, double id) {

        initComponents();
        setModal(true);
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation(dim.width/2-this.getSize().width/2, dim.height/2-this.getSize().height/2);
        
        
        // INS, UPD, DEL, DSP
        _mode = Mode;
        if (!Mode.equals("INS")){
            //No es un insert por tando debe existir en la DB
            //Proceso para capturar el Registro
            System.out.println(id);
            currentUniversidad = UniversidadModel.getUniversidadesById(id);
            jTSiglas.setText(currentUniversidad.getSIGLAS());
            jTRector.setText(currentUniversidad.getRECTOR());
        }else {
            currentUniversidad = new Universidad();
        }
        switch(Mode){
            case "INS":
                jLTitle.setText("Agregando nueva Universidad");
                jBConfirmar.setText("Agregar");
                break;
            case "UPD":
                jLTitle.setText(String.format("Actualizando Universidad %.0f %s", currentUniversidad.getID(), currentUniversidad.getSIGLAS()));
                jBConfirmar.setText("Editar");
                break;
            case "DEL":
                jLTitle.setText(String.format("Eliminar Universidad %.0f %s", currentUniversidad.getID(), currentUniversidad.getSIGLAS()));
                deshabilitarCampos();
                jBConfirmar.setText("Eliminar");
                break;
            case "DSP":
                jBConfirmar.setVisible(false);
                jBCancel.setText("Salir");
                deshabilitarCampos();
                jLTitle.setText(String.format("Universidad %.0f %s", currentUniversidad.getID(), currentUniversidad.getSIGLAS()));
                break;
        }
    }
    
    private void deshabilitarCampos(){
         jTSiglas.setEditable(false);
         jTRector.setEditable(false);
    }

